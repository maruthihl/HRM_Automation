package com.example.orangehrm.javaConcepts;

public class Swaping 
{
    
    public static void main(String[] args)
     {
        int a =10;
        int b =20;
        System.out.println("Before Swaping: a= "+a+" b= "+b);
        int temp=a;
         a=b;
         b=temp;
         System.out.println("After swaping: a="+a+" b="+b);
    }
}
